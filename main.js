//Post incriment
var num = 0;
console.log(num); //0
console.log(num++); //0
console.log(num); //1
console.log(num++); //1
console.log(num); //2
console.log(num++); //2
console.log(num); //3
console.log(num = num + 5); //8
//pre increment
var a = 0;
console.log(++a); //1
console.log(a); //1
console.log(++a); //2
console.log(a); //2
console.log(++a); //3
console.log(a = a + 5); //8
//post Decrement
var b = 15;
console.log(b); //15
console.log(b--); //15
console.log(b); //14
console.log(b--); //14
console.log(b); //13
console.log(b = b - 5); //8
//pre Decrement
var c = 15;
console.log(--c); //14
console.log(c); //14
console.log(--c); //13
console.log(c); //13
console.log(c = c - 5); //8
